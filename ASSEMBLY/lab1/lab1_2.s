 .text
 .globl __start
__start:
 la $s0, _byte

 lbu $t0, 0($s0)
 lhu $t1, 0($s0)
 lw $t2, 0($s0)

 lbu $t3, 4($s0)
 lhu $t4, 4($s0)
 lw $t5, 4($s0)

 lb $t0, 0($s0)
 lh $t1, 0($s0)
 lw $t2, 0($s0)

 lb $t3, 4($s0)
 lh $t4, 4($s0)
 lw $t5, 4($s0)

 lb $t6, 4($s0)
 sb $t6, 16($s0)

 li $v0, 10
 syscall 
