 .text
 .globl __start
__start:

 lw $t1, num1
 lw $t2, num2

 mult $t1, $t2

 mflo $t3
 mfhi $t4

 multu $t1, $t2

 mflo $t5
 mfhi $t6

 la $s0, num1

 sw $t3, 8($s0)
 sw $t4, 12($s0)
 sw $t5, 16($s0)
 sw $t6, 20($s0)

 li $v0,10
 syscall 		# au revoir...
